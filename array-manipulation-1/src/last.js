// define a function named 'last' with one parameter 'array'
// get the length of the array and subtract it by 1 and put it in the index of the array
// assign it to the variable named lastElement
// return lastElement
function last(array) {
  var lastElement = array[array.length - 1];
  return lastElement;
}

